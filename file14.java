public class file14 {
    public static void main(String[] args) {

        int a = 6;
        int b = 4;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        // Bitwise AND
        System.out.println("a & b = " + (a & b));

        // Bitwise OR
        System.out.println("a | b = " + (a | b));

        // Bitwise XOR
        System.out.println("a ^ b = " + (a ^ b));

        // Bitwise NOT
        System.out.println("~a = " + (~a));
        System.out.println("~b = " + (~b));

        // Left Shift
        System.out.println("a << 1 = " + (a << 1));
        System.out.println("b << 1 = " + (b << 1));

        // Right Shift
        System.out.println("a >> 1 = " + (a >> 1));
        System.out.println("b >> 1 = " + (b >> 1));
    }
}